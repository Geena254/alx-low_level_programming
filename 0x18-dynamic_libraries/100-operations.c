#include <stdio.h>

/**
 * add - function that adds two integers
 * @a: first integer
 * @b: second integer
 * Return: the sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function that subtracts two integers
 * @a: first integer
 * @b: second integer
 * Return: the difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function that multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: the product
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: the division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function that gets the remainder between two integers
 * @a: first integer
 * @b: second integer
 * Return: the modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
